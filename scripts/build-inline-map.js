/**
 * build-inline-map.js
 * Reads leaflet.js and leaflet.min.css from assets/leaflet/,
 * base64-encodes them, and writes to leaflet-bundle.ts.
 * The WebView decodes them via atob() at runtime.
 * 
 * Usage: node scripts/build-inline-map.js
 */

const fs = require('fs');
const path = require('path');

const leafletJs  = fs.readFileSync(path.join(__dirname, '../assets/leaflet/leaflet.js'));
const leafletCss = fs.readFileSync(path.join(__dirname, '../assets/leaflet/leaflet.min.css'));

const jsB64  = leafletJs.toString('base64');
const cssB64 = leafletCss.toString('base64');

const outputPath = path.join(__dirname, '../src/components/leaflet-bundle.ts');

const content = `// AUTO-GENERATED by scripts/build-inline-map.js — do not edit manually
// Leaflet 1.9.4 bundled as base64 so no CDN/network needed in WebView
// Decode in browser via: atob(LEAFLET_JS_B64)

export const LEAFLET_CSS_B64 = '${cssB64}';

export const LEAFLET_JS_B64 = '${jsB64}';
`;

fs.writeFileSync(outputPath, content, 'utf8');
console.log('Written:', outputPath);
console.log('CSS b64 length:', cssB64.length, '| JS b64 length:', jsB64.length);

