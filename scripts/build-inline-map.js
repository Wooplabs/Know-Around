/**
 * build-inline-map.js
 * Reads leaflet.js and leaflet.min.css from assets/leaflet/,
 * then writes them into Map.tsx as inline strings.
 * 
 * Usage: node scripts/build-inline-map.js
 */

const fs = require('fs');
const path = require('path');

const leafletJs  = fs.readFileSync(path.join(__dirname, '../assets/leaflet/leaflet.js'),      'utf8');
const leafletCss = fs.readFileSync(path.join(__dirname, '../assets/leaflet/leaflet.min.css'), 'utf8');

// Write to a constants file that Map.tsx will import
const outputPath = path.join(__dirname, '../src/components/leaflet-bundle.ts');

const content = `// AUTO-GENERATED by scripts/build-inline-map.js — do not edit manually
// Leaflet 1.9.4 bundled inline so the map works with no internet/CDN dependency

export const LEAFLET_CSS = \`${leafletCss}\`;

export const LEAFLET_JS = \`${leafletJs}\`;
`;

fs.writeFileSync(outputPath, content, 'utf8');
console.log('Written:', outputPath);
console.log('CSS length:', leafletCss.length, '| JS length:', leafletJs.length);
